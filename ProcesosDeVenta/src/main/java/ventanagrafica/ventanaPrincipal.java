    public static ArrayList<Administrador> crearListaAdmins() throws FileNotFoundException {
        File cuentasAdmin = new File("recursos/CuentasAdmin.txt");
        Scanner cuentas = new Scanner(cuentasAdmin);
        ArrayList<Administrador> admins = new ArrayList<Administrador>(); 
        while (cuentas.hasNext()) {
            String correoi = cuentas.nextLine();
            String contri = cuentas.nextLine();
            Administrador admini = new Administrador(correoi, contri);
            admins.add(admini);
        }
        return admins;
    }

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String correo = jTextField2.getText();
        String contrasena = jTextField1.getText();
        while (cuentasAdmin.hasNextLine()) {
            String correoi = cuentasAdmin.nextLine();
            String contri = cuentasAdmin.nextLine();
            Administrador admini = new Administrador(correoi, contri);
            admins.add(admini);        
        }
        encontrado = false;
        if (correo.contains("javacomp")) {
            for (int i=0; i<admins.size();i++) {
                if (admins.get(i).getCorreo().equals(correo)) {
                    encontrado = true;
                    if (admins.get(i).getClave().equals(contrasena)) {
                        this.setVisible(false);
                        Admin adminJF = new Admin();
                        adminJF.setVisible(true);
                    } else {
                        JOptionPane.showMessageDialog(null, "La contraseña"
                                + " no coincide con la cuenta", 
                                "Error", JOptionPane.WARNING_MESSAGE);
                    }
                }
                else {
                    if (!encontrado) {
                    JOptionPane.showMessageDialog(null, "No se encuentra una cuenta"
                        + " con el correo especificado", 
                        "Error", JOptionPane.WARNING_MESSAGE);
                    encontrado = true;
                    }
                }
            }    
        } else {
            if (!encontrado) {
                JOptionPane.showMessageDialog(null, "No se encuentra una cuenta"
                    + " con el correo especificado", 
                    "Error", JOptionPane.WARNING_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

